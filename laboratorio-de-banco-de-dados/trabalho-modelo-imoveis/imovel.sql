create database imoveis;
use imoveis;

create table estado (
	id int not null primary key auto_increment,
    nome varchar(100) not null,
    sigla varchar(2) not null
);

create table cidade (
	id int not null primary key auto_increment,
    nome varchar(100) not null,
    estadoId int not null,
    foreign key(estadoId) references estado(id)
);

create table bairro (
	id int not null primary key auto_increment,
    nome varchar(100) not null,
    cidadeId int not null,
    foreign key(cidadeId) references cidade(id)
);

create table endereco (
	id int not null primary key auto_increment,
    logradouro varchar(100) not null,
    numero varchar(10) not null,
    complemento varchar(50) NULL,
    bairroId int not null,
    foreign key(bairroId) references bairro(id)
);

create table comprador (
	id int not null primary key auto_increment,
    nome varchar(100) not null,
    cpf numeric(11) not null,
    telefone varchar(20) not null,
    enderecoId int not null,
    foreign key(enderecoId) references endereco(id)
);

create table vendedor (
	id int not null primary key auto_increment,
    nome varchar(100) not null,
    cpf numeric(11) not null,
    telefone varchar(20) not null,
    dataNascimento date not null,
    enderecoId int not null,
    foreign key(enderecoId) references endereco(id)
);

create table faixa_imovel (
	id int not null primary key auto_increment,
    nome varchar(100) not null,
    valorMaximo decimal(8,2) not null,
    valorMinimo decimal(8,2) not null
);

create table status_venda (
	id int not null primary key auto_increment,
    status varchar(50) not null
);

create table imovel (
	id int not null primary key auto_increment,
    areaUtil decimal(8,2) not null,
    areaTotal decimal(8,2) not null,
    preco decimal(12,2) not null,
    dataLancamento date not null,
    vendedorId int not null,
    enderecoId int not null,
    faixaId int not null,
    statusVendaId int not null,
	foreign key(vendedorId) references vendedor(id),
    foreign key(enderecoId) references endereco(id),
    foreign key(faixaId) references faixa_imovel(id),
    foreign key(statusVendaId) references status_venda(id)
);

create table oferta (
	id int not null primary key auto_increment,
    imovelId int not null,
    compradorId int not null,
    valor decimal(8,2) not null,
    data_oferta date not null,
    foreign key(imovelId) references imovel(id),
    foreign key(compradorId) references comprador(id)
);


